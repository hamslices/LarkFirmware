/**
 * @brief  The C part of the Hard Fault Handler.
 * @param  p_stacked_frame: Pointer to the stack frame saved by the CPU.
 */
void HardFault_Handler_C(uint32_t *p_stacked_frame)
{
    // 1. Create a struct to hold the relevant crash information.
    hard_fault_info_t fault_info;
    fault_info.program_counter         = p_stacked_frame[6]; // PC is the 7th item (index 6)
    fault_info.link_register           = p_stacked_frame[5];
    fault_info.program_status_register = p_stacked_frame[7];

    // 2. Call our central error reporter to handle the fault.
    //    This function will never return.
    error_reporter_handle_hard_fault(&fault_info);
}

